    private void load() {
        DefaultTableModel model = (DefaultTableModel) tblGridView.getModel();
        model.setColumnIdentifiers(new Object[]{"Mã NV", "Tên NV", "Hình", "Giới tính", "Ngày sinh", "Địa chỉ", "SDT", "EMail", "Mật khẩu", "ID CHỨC VỤ", "Lương"});
        model.setRowCount(0);
        try {
            List<Nhanvien_1> list = dao.select();
            for (Nhanvien_1 nv : list) {
                Object[] row = {
                    nv.getMaNV(),
                    nv.getHoten(),
                    nv.getHinh(),
                    nv.isGioitinh() ? 1 : 0,
                    nv.getNgaySinh(),
                    nv.getDiachi(),
                    nv.getSDT(),
                    nv.getEmail(),
                    nv.getMatkhau(),
                    nv.getMachucvu(),
                    nv.getLuong()
                };
                model.addRow(row);

            }
        } catch (Exception e) {
            DialogHP.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    private void search() {
        DefaultTableModel model = (DefaultTableModel) tblGridView.getModel();
        model.setColumnIdentifiers(new Object[]{"Mã NV", "Tên NV", "Hình", "Giới tính", "Ngày sinh", "Địa chỉ", "SDT", "EMail", "Mật khẩu", "ID CHỨC VỤ", "Lương"});
        model.setRowCount(0);
        try {
            List<Nhanvien_1> list = dao.find(txtFind.getText(), txtFind.getText());
            for (Nhanvien_1 nv : list) {
                Object[] row = {
                    nv.getMaNV(),
                    nv.getHoten(),
                    nv.getHinh(),
                    nv.isGioitinh() ? 1 : 0,
                    nv.getNgaySinh(),
                    nv.getDiachi(),
                    nv.getSDT(),
                    nv.getEmail(),
                    nv.getMatkhau(),
                    nv.getMachucvu(),
                    nv.getLuong()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            DialogHP.alert(this, "Không tìm thấy tên hoặc mã nhân viên");
            e.printStackTrace();
            return;
        }
    }

    void insert() {

        try {
            if (!validate.checkNull(txtTenNV.getText())) {
                sb.append("Vui lòng nhập tên nhân viên \n");
            }
            if (!validate.checkNull(txtDiachi.getText())) {
                sb.append("Vui lòng nhập địa chỉ \n");
            }
            if (!validate.checkNull(txtEmail.getText())) {
                sb.append("Vui lòng nhập Email \n");
            } else if (!validate.checkEmail(txtEmail.getText())) {
                sb.append("Vui lòng nhập đúng định dạng Email\n");
            }
            if (!validate.checkNull(txtLuong.getText())) {
                sb.append("Vui lòng nhập lương \n");
            } else if (!validate.checkSo(txtLuong.getText())) {
                sb.append("Lương phải là số \n");
            }
            if (!validate.checkNull(txtMatkhau.getText())) {
                sb.append("Vui lòng nhập mật khẩu \n");
            }
            if (!validate.checkNull(txtNamSinh.getText())) {
                sb.append("Vui lòng nhập năm sinh \n");
            } else if (!validate.checkNgay(txtNamSinh.getText())) {
                sb.append("Vui lòng nhập đúng định dạng năm sinh(dd/mm/yyyy) \n");
            }
            if (!validate.checkNull(txtsdt.getText())) {
                sb.append("Vui lòng nhập số điện thoại \n");
            } else if (!validate.checkSDT(txtsdt.getText())) {
                sb.append("Vui lòng nhập đúng định dạng số điện thoại(0+(9)\n)");
            }
            if (sb.toString().isBlank()) {
                Nhanvien_1 model = getModel();
                dao.insert(model);
                this.load();
                this.clear();
                DialogHP.alert(this, "Thêm mới thành công!");
            } else {
                JOptionPane.showMessageDialog(this, sb.toString());
                sb.setLength(0);
                return;
            }

        } catch (Exception e) {
            DialogHP.alert(this, "Thêm mới thất bại!");
            e.printStackTrace();
        }
    }

