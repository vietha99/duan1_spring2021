    private void setModel(Nhanvien_1 model) {

        lblmanv1.setText(model.getMaNV());
        txtTenNV.setText(model.getHoten());
        txtDiachi.setText(model.getDiachi());
        txtEmail.setText(model.getEmail());
        txtMatkhau.setText(model.getMatkhau());
        txtNamSinh.setText(model.getNgaySinh().toString());
        txtsdt.setText(model.getSDT());
        txtLuong.setText(model.getLuong() + "");
        if (model.getMachucvu() == 1) {
            cbxchu.setSelected(true);
        }
        if (model.getMachucvu() == 2) {
            cbxbanhang.setSelected(true);
        }   
        if (model.isGioitinh() == true) {
            cbxNam.setSelected(true);
        }
        if (model.isGioitinh() == false) {
            cbxNu.setSelected(true);
        }

        lblhinh.setToolTipText(model.getHinh());
        if (model.getHinh() != null) {
            lblhinh.setIcon(ShareHelper.readLogo(model.getHinh()));
        }
        if (model.getHinh() == null) {
            lblhinh.setIcon(ShareHelper.readLogo("default.png"));
        }

    }

    void setStatus(boolean insertable) {

        btnUpdate.setEnabled(insertable);
        btnUpdate.setEnabled(!insertable);
        btnDelete.setEnabled(!insertable);
        boolean first = this.index > 0;
        boolean last = this.index < tblGridView.getRowCount() - 1;
        btnFirst.setEnabled(!insertable && first);
        btnPrev.setEnabled(!insertable && first);
        btnLast.setEnabled(!insertable && last);
        btnNext.setEnabled(!insertable && last);
    }

    void selectImage() {
        if (jFileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File file = jFileChooser.getSelectedFile();
            if (ShareHelper.saveLogo(file)) {
                lblhinh.setIcon(new ImageIcon(ShareHelper.resize(file.getPath(), 159, 206)));
                lblhinh.setToolTipText(file.getName());
            }
        }
    }
