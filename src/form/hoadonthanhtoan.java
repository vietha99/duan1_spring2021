    void insert() {
        CLASS.hdthanhtoan model = getModel();
        model.setNgaytao(new Date());
        try {
            daott.insert(model);
            try {
                Connection conn = DriverManager.getConnection(dburl);
                String sql = "select * from HOADONTT where IDNHANVIEN = ? and IDKHACHHANG = ? and TONGTIEN = ?";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(1, String.valueOf(helper.ShareHP.USER.getIdnhanvien()));
                ps.setString(2, idkhach.getText());
                ps.setString(3, txttongtien.getText());
                ResultSet rs = ps.executeQuery();
                while (rs.next()) {
                    int a = rs.getInt("IDHOADONTT");
                    for (int i = 0; i < tblsanpham.getRowCount(); i++) {
                        CLASS.hdthanhtoanct hdtt = new hdthanhtoanct();
                        int b = (int) Double.parseDouble(tblsanpham.getValueAt(i, 3).toString());
                        hdtt.setIdhoadontt(a);
                        hdtt.setIdsanpham((int) tblsanpham.getValueAt(i, 0));
                        hdtt.setSoluong(b);
                        daott.insertHDCT(hdtt);
                        CLASS.sanpham sp = new sanpham();
                        sp.setA(b);
                        sp.setMaSP((int) tblsanpham.getValueAt(i, 0));
                        daott.updatesp(sp);
                    }
                }
            } catch (Exception e) {
                DialogHP.alert(this, "lỗi thanh toán thử lại");
            }

            testloadTable();
            helper.DialogHP.alert(this, "Thêm mới thành công!");
        } catch (HeadlessException e) {
            helper.DialogHP.alert(this, "Thêm mới thất bại!");
        }

    }
