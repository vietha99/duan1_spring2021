    public ArrayList<CLASS.sanpham> splist(String name) {
        splist.removeAll(splist);
        try {
            Connection conn = DriverManager.getConnection(dburl);
            String sql = "SELECT * FROM SANPHAM where TENSP = N'" + name + "'";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            sanpham sp;
            while (rs.next()) {
                sp = new sanpham(rs.getInt("IDSANPHAM"), rs.getString("TENSP"), rs.getFloat("GIABAN"));
                splist.add(sp);
            }
        } catch (Exception e) {
        }
        return splist;
    }

